class Patterns8{
public static void main(String args[]){
	for(int i =1;i<=5;i++){
		
		
		for (int j=1;j<i+1;j++){
			System.out.print("*");
		}
		
		
	System.out.println();

	}

	for(int k =1;k<=4;k++){


		for (int j=k;j<=4;j++){
			System.out.print("*");
		}
		

	System.out.println();
}
}}